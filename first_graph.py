# WHAT THIS SCRIPT DOES
# Builds a three-node LangGraph invoice review workflow. Validates each invoice
# against confidence and value thresholds, routes to auto-approval or human
# review accordingly, and generates GPT-4o written notes for each outcome.
#
# HOW IT WORKS — STEP BY STEP
# 1. State defined        — shared notepad with 7 fields all nodes can read/write
# 2. LLM initialised      — one Azure OpenAI connection shared by both action nodes
# 3. validate node        — checks confidence and total, sets decision to pending
# 4. route_invoice()      — reads state, returns name of next node as a string
# 5. auto_approve node    — generates approval note via GPT-4o
# 6. flag_for_review node — generates review request note via GPT-4o
# 7. graph compiled       — wired together and locked into a runnable app
# 8. app.invoke()         — runs the full graph for each test invoice
#
# ROUTING LOGIC
# confidence >= 0.85 AND total <= 50000  →  auto_approve
# confidence <  0.85 OR  total >  50000  →  flag_for_review
